package vo;

public class BbsVO {
	private int num, hit;
	private String sub;
	private String writer;
	private String pwd;
	private String cont;
	private String reip;
	private String redate;

	/**
	 * @return the hit
	 */
	public int getHit() {
		return hit;
	}

	/**
	 * @param hit
	 *            the hit to set
	 */
	public void setHit(int hit) {
		this.hit = hit;
	}

	/**
	 * @return the num
	 */
	public int getNum() {
		return num;
	}

	/**
	 * @param num
	 *            the num to set
	 */
	public void setNum(int num) {
		this.num = num;
	}

	/**
	 * @return the sub
	 */
	public String getSub() {
		return sub;
	}

	/**
	 * @param sub
	 *            the sub to set
	 */
	public void setSub(String sub) {
		this.sub = sub;
	}

	/**
	 * @return the writer
	 */
	public String getWriter() {
		return writer;
	}

	/**
	 * @param writer
	 *            the writer to set
	 */
	public void setWriter(String writer) {
		this.writer = writer;
	}

	/**
	 * @return the pwd
	 */
	public String getPwd() {
		return pwd;
	}

	/**
	 * @param pwd
	 *            the pwd to set
	 */
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	/**
	 * @return the cont
	 */
	public String getCont() {
		return cont;
	}

	/**
	 * @param cont
	 *            the cont to set
	 */
	public void setCont(String cont) {
		this.cont = cont;
	}

	/**
	 * @return the reip
	 */
	public String getReip() {
		return reip;
	}

	/**
	 * @param reip
	 *            the reip to set
	 */
	public void setReip(String reip) {
		this.reip = reip;
	}

	/**
	 * @return the redate
	 */
	public String getRedate() {
		return redate;
	}

	/**
	 * @param redate
	 *            the redate to set
	 */
	public void setRedate(String redate) {
		this.redate = redate;
	}

}
