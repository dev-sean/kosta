    private Color selectColor() {
        String color = (String) colorBtn.getSelectedItem();
        if (color.equals("RED")) {
            return Color.red;
        } else if (color.equals("BLUE")) {
            return Color.blue;
        } else if (color.equals("YELLOW")) {
            return Color.yellow;
        } else if (color.equals("BLACK")) {
            return Color.black;
        } else if (color.equals("WHITE")) {
            return Color.white;
        } else if (color.equals("GREEN")) {
            return Color.green;
        } else {
            return Color.pink;
        }
    }
